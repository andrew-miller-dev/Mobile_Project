package com.example.rentalapp;

public class RentalProperty {
    String city;
    String address;
    int bedroom;
    int bathroom;
    double price;

    public RentalProperty(String City, String Address, int Bedroom, int Bathroom, double Price){
        city = City;
        address = Address;
        bedroom = Bedroom;
        bathroom = Bathroom;
        price = Price;
    }
}
